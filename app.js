function applyTheme() {
  const prefersDark = window.matchMedia && window.matchMedia("(prefers-color-scheme: dark)").matches;
  const resolved = settings.theme === "auto" ? (prefersDark ? "dark" : "light") : settings.theme;
  document.documentElement.dataset.theme = resolved;
  document.body.classList.toggle("no-animations", !settings.animations);
  $$("#themeSelector button").forEach(btn => btn.classList.toggle("active", btn.dataset.theme === settings.theme));
  $("#animationsToggle").checked = !!settings.animations;
}

function filteredVocabulary() {
  const term = $("#searchInput").value.trim().toLowerCase();
  const unlearned = $("#unlearnedOnly").checked;
  return VOCABULARY.filter(item => {
    const matchesCategory = activeCategory === "All" || item.category === activeCategory;
    const matchesDifficulty = activeDifficulty === "All" || item.difficulty === activeDifficulty;
    const matchesUnlearned = !unlearned || !isLearned(item.id);
    const text = [item.word, item.translation, item.category, item.difficulty, item.pronunciation, item.pastForm || ""].join(" ").toLowerCase();
    const matchesSearch = !term || text.includes(term);
    return matchesCategory && matchesDifficulty && matchesUnlearned && matchesSearch;
  });
}

function shuffle(array) {
  const copy = [...array];
  for (let i = copy.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [copy[i], copy[j]] = [copy[j], copy[i]];
  }
  return copy;
}

