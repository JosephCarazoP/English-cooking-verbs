function startQuiz() {
  const source = currentList.length ? currentList : filteredVocabulary();
  const questions = shuffle(source).slice(0, Math.min(10, source.length));
  if (!questions.length) return;
  quizState = { questions, index: 0, score: 0, answered: false };
  openModal("quizModal");
  renderQuizQuestion();
}

function renderQuizQuestion() {
  const q = quizState.questions[quizState.index];
  const askPast = q.type === "verb" && Math.random() > 0.48;
  const prompt = askPast ? `What is the past form of “${q.word}”?` : `What does “${q.word}” mean in Spanish?`;
  const correct = askPast ? q.pastForm : q.translation;
  const optionsPool = VOCABULARY
    .filter(item => item.id !== q.id && (askPast ? item.type === "verb" : true))
    .map(item => askPast ? item.pastForm : item.translation)
    .filter(Boolean);
  const options = shuffle([correct, ...shuffle([...new Set(optionsPool)]).slice(0, 3)]);
  $("#quizContent").innerHTML = `
    <p class="quiz-progress">Question ${quizState.index + 1} / ${quizState.questions.length}</p>
    <h2>${prompt}</h2>
    <div class="quiz-options">
      ${options.map(option => `<button class="quiz-option" data-answer="${escapeAttr(option)}" data-correct="${escapeAttr(correct)}">${option}</button>`).join("")}
    </div>
  `;
}

function escapeAttr(value) { return String(value).replace(/&/g, "&amp;").replace(/"/g, "&quot;").replace(/</g, "&lt;"); }

function handleQuizAnswer(button) {
  if (!quizState || quizState.answered) return;
  quizState.answered = true;
  const isCorrect = button.dataset.answer === button.dataset.correct;
  if (isCorrect) quizState.score += 1;
  $$(".quiz-option").forEach(btn => {
    if (btn.dataset.answer === btn.dataset.correct) btn.classList.add("correct");
    else if (btn === button) btn.classList.add("wrong");
    btn.disabled = true;
  });
  setTimeout(() => {
    quizState.index += 1;
    quizState.answered = false;
    if (quizState.index >= quizState.questions.length) finishQuiz();
    else renderQuizQuestion();
  }, 850);
}

function finishQuiz() {
  const percent = Math.round((quizState.score / quizState.questions.length) * 100);
  state.bestQuiz = Math.max(state.bestQuiz || 0, percent);
  addXp(Math.round(percent / 5), false);
  checkGoals();
  saveState();
  renderStats();
  $("#quizContent").innerHTML = `
    <div class="complete-icon">${percent >= 80 ? "🏆" : "✨"}</div>
    <h2>Quiz complete!</h2>
    <p class="muted">Resultado: <strong>${quizState.score} / ${quizState.questions.length}</strong> · ${percent}%</p>
    <div class="complete-actions" style="margin-top: 18px;">
      <button class="primary-btn" id="quizAgainBtn">Try again</button>
      <button class="secondary-btn" data-close="quizModal">Close</button>
    </div>
  `;
}

